package main

import "fmt"

func main() {

  x := 45
  y := 7
  
  //Addition
  add := x+y
  fmt.Println(add)
  
  //Subtraction
  sub := x-y
  fmt.Println(sub)
  
  //Multipication
  mul := x*y
  fmt.Println(mul)
  
  //Division 
  div := x/y
  fmt.Println(div)
      
  // Modulus, results in the remainder of the division
  rem := x % y
  fmt.Println(rem) 
}
